class Calcultor{

    static sum(num1, num2){
        return num1 + num2;
    }

    static sub(num1, num2){
        return num1 - num2;
    }

    static div(num1, num2){
        return num1 / num2;
    }

    static mult(num1, num2){
        return num1 * num2;
    }
}

module.exports = Calcultor;